public class SensorInfo {

    private String x;   //X axis value
    private String y;   //Y axis value
    private String z;   //Z axis value
    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }

    public String getZ() {
        return z;
    }

    public void setZ(String z) {
        this.z = z;
    }


}
